    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        System.out.println("Add List Truyen Thue");
        int listSelected [] = jTable1.getSelectedRows(); // if select many => []
        System.out.println("Len Add: "+listSelected.length);
        if (listSelected.length >= 1) // check
        {
            for (int i=0; i<listSelected.length; i++)
            {
                // kiểm tra lại các truyện có status là chưa thuê
                if ("Chưa thuê".equals(listTruyen.get(listSelected[i]).getStatus()))
                {
                    boolean find = false;
                    for (int j = 0; j<listTruyensethue.size(); j++)
                    {
                        // kiểm tra truyện đã trong ds Truyensethue hay chưa
                        if (listTruyen.get(listSelected[i]).getId() == listTruyensethue.get(j).getId())
                        {
                            find = true;
                            break;
                        }
                    }
                    if (find == false)
                    {
                        listTruyensethue.add(listTruyen.get(listSelected[i]));
                    }
                }
                
            }
            setViewTruyenthue();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


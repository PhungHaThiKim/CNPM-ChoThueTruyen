    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String hireDate = jTextField2.getText();
        String payDate = jTextField3.getText();
        float saleoff = Float.parseFloat(jTextField4.getText());
        
        // Chuyển từ String -> Calendar sang Date
        String dhplit [] = hireDate.split("-");
        Calendar hd = Calendar.getInstance(); // lay time trong may
        hd.set(Integer.parseInt(dhplit[0]), Integer.parseInt(dhplit[1])-1, Integer.parseInt(dhplit[2]));
        Date hiredate = hd.getTime(); // tra ve date
        phieuthuetruyen.setHiredate(hiredate);
        
        String dpplit [] = payDate.split("-");
        Calendar pd = Calendar.getInstance();
        pd.set(Integer.parseInt(dpplit[0]), Integer.parseInt(dpplit[1])-1, Integer.parseInt(dpplit[2]));
        Date paydate = pd.getTime();
        phieuthuetruyen.setPaydate(paydate);
        
        phieuthuetruyen.setSaleoff(saleoff);
        phieuthuetruyen.setStatus(0);
        
        // Add vao truyensethue vao Truyenthue
        ArrayList<Truyenthue> listruyenthue = new ArrayList<>();
        for (int i=0; i<listTruyensethue.size(); i++)
        {
            Truyen t = listTruyensethue.get(i);
            Truyenthue tt = new Truyenthue();
            tt.setTruyen(t);
            listruyenthue.add(tt);
        }
        phieuthuetruyen.setTruyenthue(listruyenthue);
        
        // Bat execption
        if (Utility.DiffenceDay(phieuthuetruyen.getHiredate(), phieuthuetruyen.getPaydate()) <= 0)
        {
            JOptionPane.showMessageDialog(null, "Pay Date <= Hire Date", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if (saleoff < 0)
        {
            JOptionPane.showMessageDialog(null, "Saleoff < 0", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if (listTruyensethue.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Khong co truyen trong ds se thue", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            ConfirmView cv = new ConfirmView(this, true, phieuthuetruyen);
            cv.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

